        public static int[] contarOcurrencias(String texto, Lista palabras) {
        int[] conteo = new int[palabras.getiN()];

        for (String palabra : texto.split("\\W+")) {
            for (int i = 0; i < palabras.getiN(); i++) {
                if (palabra.equalsIgnoreCase(palabras.getPosicion(i).getInfo())) {
                    conteo[i]++;
                }
            }
        }

        return conteo;
    }

    
    private void listaOpcionesResumenes(){
        
        ListaResumen resumenesEnOrden = LeerTxt.listaDeResumenes;
        
        if (resumenesEnOrden != null && resumenesEnOrden.getiN() > 0) {
            DefaultListModel<String> listModel = new DefaultListModel<>();
            NodoResumen nodoActual = resumenesEnOrden.getFirst();
            while (nodoActual != null) {
                listModel.addElement(nodoActual.getInfo().getTitulo());
                nodoActual = nodoActual.getNext();
            }
            resumenesLista.setModel(listModel);
        } else {
            DefaultListModel<String> listModel = new DefaultListModel<>();
            resumenesLista.setModel(listModel);
            JOptionPane.showMessageDialog(null, "Error.");
        }
        
        
    }
    
    
