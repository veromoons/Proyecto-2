     private void inicializarJList() {
        resumenesLista.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 1) { // Detecta un solo clic
                    int index = resumenesLista.locationToIndex(e.getPoint());
                    if (index >= 0) {
                        ListaResumen resumenesEncontrados = Main.txt.listaDeResumenes;
                        int contador = 0;
                        NodoResumen aux = resumenesEncontrados.getFirst();
                        while (contador != index && aux != null) {
                            aux = aux.getNext();
                            contador++;
                        }
                        if (aux != null) {
                        VentanaMostrarAnalisis vma = new VentanaMostrarAnalisis(aux.getInfo());
                        vma.setVisible(true);
                        
                        }
                    }
                }
            }
        });
     }
    
